// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(x, y, z, arr = []) {
  if (z == null) {
    if (x <= y) z = 1
    else z = -1
  }

  if (z === 0) return arr

  if (z > 0) {
    for (x; x <= y; x += z) arr.push(x)
  }

  else for (x; x >= y; x += z) arr.push(x)

  return arr
}

console.log(range(1, 10, 2), range(50, 2, -3)) 

