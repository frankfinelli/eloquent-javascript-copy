function deepEqual(x, y) {
  //if they're both completely equal, end it
  if (x === y) return true
  
  //null or !objects? end it
  if (x == null || y == null || typeof x !== "object" || typeof y !== "object") return false

  //they're objects? great; are they long enough?

  let admKeys = Object.keys(x)
  let cptKeys = Object.keys(y)
  if (admKeys.length !== cptKeys.length) return false 

  //loop through attributes for the props, had to look this up - loop and recursion? ok

  for (let z in admKeys) {
    if (!(admKeys[z] in y)) return false
    if (!deepEqual(x[admKeys[z]], y[admKeys[z]])) return false
  }
  return true
}

