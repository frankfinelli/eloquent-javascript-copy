// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
  if (x % 2 === 0) return true
  else return false
}

console.log(isEven(6), isEven(99), isEven(-209), isEven(45), isEven(2024))
