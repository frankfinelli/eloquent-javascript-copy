function countBs(str) {
  let z = 0
  for (let x = 0; x < str.length; x++) {
    if (str[x] === 'b' || str[x] === 'B') { //both upper and lowercase?
      z++
    }
  }

  return z
}

console.log(countBs('bbbbbbbbbbbB'), countBs('abcdefgh'), countBs('aeiouandsometimesy'))
