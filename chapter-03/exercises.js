function countChars(str, char) {
  let z = 0
  for (let x = 0; x < str.length; x++) {
    if (str[x] === char) {
      z++
    }
  }

  return z
}

console.log(countChars('ploppp', 'p'), countChars('there ain\'t a damn thing in here', 's'))
