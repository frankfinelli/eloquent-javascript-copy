// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
  if (x < y) return x
  else return y
}

console.log(min(0, 10), min(0, -10), min(224, 937), min(9, 9))

