function flatten(arr, o = []) {
  for (let x = 0; x < arr.length; x++) {
    for (let y = 0; y < arr[x].length; y++) {
      o.push(arr[x][y])
    }
  }

  return o
}

// /////////////////////////////////////////////////////////////////////////////
// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(init, cond, act, out) {
  while (cond(init)) {
    out(init)
    init = act(init)
  }
}

// /////////////////////////////////////////////////////////////////////////////
// every ///////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function every(arr, fx) {
  for (let x = 0; x < arr.length; x++) {
    if (!fx(arr[x])) return false
  }

  return true
}

